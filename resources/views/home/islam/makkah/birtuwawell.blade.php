            <h4>What Is the Tuwa Valley ?</h4>
            According to the clause al-wadi al-muqaddas, the term “Tuwa” means “the valley that has been sanctified
            twice.” It is said to be the place where Hazrat Musa (AS) received the first revelation and Prophet Muhammad
            (PBUH) camped before conquering Makkah. The sacred Tuwa Valley is also mentioned in the Holy Quran:

            “Verily I am thy Lord! Therefore (in My presence) put off thy shoes: thou art in the sacred valley Tuwa.”
            (Surah Taha, 20/12)

            <h4>Where Is the Tuwa Valley Located ?</h4>
            Today, the Tuwa Valley is a part of Makkah and is called Jarwal. To be exact, the Tuwa Valley is located in
            the Sinai Peninsula, within the boundaries of Masjid Al-Haram. Moreover, a part of Tuwa Valley is called
            Al-Buqah Al-Mubarakah, meaning “The Blessed Place.”

            <h4>Mosque at Dhi Tuwa</h4>
            The mosque at Dhi Tuwa is said to be constructed by Zubaydah, the wife of Harun ul-Rashid. It is built on
            the spot where Prophet Muhammad (PBUH) performed Salah (prayer). Even though the Mosque had been demolished
            during the expansion of the Haram, the well of Tuwa (Bir Tuwa) still remains.
