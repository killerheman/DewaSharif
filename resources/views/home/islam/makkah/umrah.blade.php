                <h4> 1. Definition and Significance of Umrah :</h4>
                <i><h5>Umrah Meaning :</h5></i> The word "Umrah" comes from the Arabic term that means “to visit a populated place.” In
                Islamic terminology, it refers to visiting the Kaaba, performing specific rites, and seeking closeness
                to Allah.

                <i><h5>Distinction from Hajj :</h5></i> Unlike Hajj, Umrah is not obligatory but is a Sunnah act (encouraged and
                recommended practice) in Islam. While Hajj has a set time during the Islamic month of Dhu al-Hijjah,
                Umrah can be performed at any time except during the specific days of Hajj.

                <i><h5>Spiritual Benefits :</h5></i> Performing Umrah is believed to be a means of purifying oneself from sins, earning
                great rewards, and seeking forgiveness. It serves as an opportunity for self-reflection, repentance, and
                spiritual growth.

                <i><h5>Prophet Muhammad’s Teachings :</h5></i> The Prophet Muhammad (peace be upon him) emphasized the value of Umrah. He
                said:
                "The performance of Umrah is an expiation for the sins committed between it and the previous one, and
                the reward for an accepted Hajj is nothing but Paradise." (Sahih al-Bukhari)

                <h4> 2. Rituals of Umrah :</h4>
                <b> Umrah involves four main rituals : </b>
               <i><h5> 1. Ihram (State of Purity) :</h5></i>
                Pilgrims begin by entering a state of ritual purity called Ihram before crossing the Miqat (the
                designated boundary points).
                Preparation for Ihram: Men wear two white, seamless cloths—one around the waist and the other over the
                shoulders. Women wear simple, modest clothing that meets Islamic guidelines.

               <i><h5> Intention (Niyyah) :</h5></i> Pilgrims make the intention to perform Umrah and recite the Talbiyah:
                "Labbayk Allahumma labbayk. Labbayka la shareeka laka labbayk. Innal-hamda wan-ni'mata laka wal-mulk. La
                shareeka lak."
                (Here I am, O Allah, here I am. Here I am, You have no partner, here I am. Verily, all praise, grace,
                and dominion are Yours. You have no partner.)

               <i><h5> Prohibitions during Ihram :</h5></i> Certain activities are prohibited during the state of Ihram, such as cutting
                hair or nails, using perfumes, and engaging in marital relations.

               <i><h5> 2. Tawaf (Circling the Kaaba) :</h5></i>
                Upon arrival at the Masjid al-Haram in Mecca, pilgrims perform Tawaf by walking around the Kaaba seven
                times counterclockwise. This act is a form of devotion to Allah and symbolizes the unity of the
                believers.

              <i><h5>  Istilam (Touching or Gesturing to the Black Stone) :</h5></i> If possible, pilgrims try to touch or kiss the Black
                Stone (Hajar al-Aswad), but due to crowds, many gesture towards it while passing.

               <i><h5> 3. Sa’i (Walking between Safa and Marwah) :</h5></i>
                After Tawaf, pilgrims proceed to walk seven times back and forth between the hills of Safa and Marwah,
                located within the Masjid al-Haram.
                This ritual commemorates Hajar’s desperate search for water for her son Ismail, symbolizing perseverance
                and trust in Allah’s provision.

               <i><h5> 4. Halq or Taqsir (Shaving or Cutting Hair) :</h5></i>
                After completing Sa’i, men either shave their heads (Halq) or trim their hair (Taqsir). Shaving the head
                is considered more virtuous.
                Women cut a small portion of their hair (about an inch) as a symbolic gesture.
                This act signifies leaving the state of Ihram, completing the Umrah, and returning to a state of
                normalcy.

                <h4> 3. Types of Umrah :</h4>
               <b> There are two types of Umrah : </b>
               <i><h5> Umrah Mufradah :</h5></i> This refers to Umrah performed independently of Hajj at any time of the year. It is the
                most common form of Umrah and is not connected with the Hajj pilgrimage.

                <i><h5>Umrah al-Tamattu’ :</h5></i> This is performed as part of the Hajj Tamattu’ pilgrimage, where Umrah is done first,
                followed by Hajj during the specific days of Dhu al-Hijjah. It requires pilgrims to enter the state of
                Ihram twice—once for Umrah and again for Hajj.

                <h4> 4. Practical Aspects of Umrah :</h4>
                <i><h5>Preparation for Umrah :</h5></i>
                Pilgrims should prepare themselves spiritually and mentally before the journey, learning the rituals of
                Umrah and ensuring they meet the conditions of entering Ihram.
                It’s recommended to seek forgiveness, make amends with others, and prepare financially for the journey.

                <i><h5>Travel and Accommodation :</h5></i>
                Pilgrims usually travel to Saudi Arabia by air, with the nearest airport being King Abdulaziz
                International Airport in Jeddah or Prince Mohammed bin Abdulaziz International Airport in Medina.
                Hotels and accommodations in Mecca range from budget options to luxury hotels, with many situated close
                to the Masjid al-Haram for convenience.

               <i><h5> Visa and Regulations :</h5></i>
                A visa is required to enter Saudi Arabia for Umrah, and it can be obtained through authorized travel
                agents or directly from the Saudi government's online portals.
                During certain times of the year (such as Ramadan), the number of pilgrims increases significantly, so
                advance planning and booking are essential.

                <i><h5>Health and Safety :</h5></i>
                Pilgrims should ensure they are in good health, as the rituals involve walking and standing for extended
                periods, often in crowded and hot conditions.
                It’s important to follow safety guidelines provided by Saudi authorities and be mindful of personal
                belongings in crowded places.

               <h4>  5. Spiritual Benefits and Rewards of Umrah :</h4>
                <i><h5>Expiation of Sins :</h5></i> Umrah is believed to expiate minor sins and bring the pilgrim closer to Allah.
                Prophet Muhammad (peace be upon him) said-
                "Perform Umrah, for it eliminates poverty and sins just as the bellows remove impurities from iron,
                gold, and silver." (Sunan Ibn Majah)

                <i><h5>Strengthening of Faith :</h5></i> The spiritual journey of Umrah helps deepen a Muslim’s connection with Allah,
                offering a time for reflection, repentance, and gratitude.

                <i><h5>Spiritual Rejuvenation :</h5></i> For many, visiting the holy sites and performing Umrah brings peace and
                spiritual renewal, allowing them to return to their daily lives with a renewed sense of purpose and
                faith.

                <h4> 6. Recommended Times for Umrah :</h4>
               <i><h5> Ramadan :</h5></i> Performing Umrah during the month of Ramadan is considered especially meritorious. The Prophet
                (peace be upon him) said-
                "Umrah in Ramadan is equal (in reward) to Hajj with me." (Sahih al-Bukhari)
                Due to this, many Muslims prefer to perform Umrah during Ramadan, though it is typically more crowded.
                Off-Peak Times: Outside of Ramadan and the Hajj season, Umrah can be performed with fewer crowds, making
                it more convenient for those seeking a quieter pilgrimage experience.

                7. After Umrah – Maintaining Spirituality :</h4>
                <i><h5>Continuing Good Deeds :</h5></i> After returning from Umrah, it is encouraged to maintain the spiritual momentum
                by increasing acts of worship such as prayer, charity, and reading the Quran.

                <i><h5>Sharing the Experience :</h5></i> Many pilgrims share their experiences and the lessons learned during Umrah with
                family and friends, fostering a sense of community and encouraging others to embark on the pilgrimage.

                <i><h5>Gifts and Souvenirs :</h5></i> It is common for pilgrims to bring back Zamzam water, dates, prayer mats, and other
                souvenirs as gifts for loved ones.
                Umrah is a deeply enriching spiritual experience, offering a chance for Muslims to purify themselves,
                seek Allah's mercy, and experience the beauty and history of Mecca. It serves as a reminder of the
                submission to Allah and the unity of the Muslim community worldwide.

