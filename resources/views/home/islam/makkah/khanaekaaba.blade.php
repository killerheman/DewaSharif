                <h4>2. Structure and Architecture :</h4>
                The Kaaba is cube-shaped, approximately 13.1 meters (43 feet) tall, with sides about 11 to 12 meters
                (36-40 feet) long.

                <i><h5>Materials :</h5></i> The building is made of grey stone and marble, sourced from the surrounding hills.

                <i><h5>Exterior :</h5></i> It is covered by the Kiswah, a black silk cloth embroidered with Quranic verses in gold and
