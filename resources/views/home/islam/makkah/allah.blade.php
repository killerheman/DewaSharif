        <i>
            <h5>Creator and Sustainer :</h5>
        </i> Allah is believed to have created all human beings, giving them life and the
        ability to distinguish between right and wrong. He sustains their lives, provides their needs, and is
        the ultimate source of all blessings.

        <i>
            <h5>Guide and Judge :</h5>
        </i> Allah is the ultimate guide, sending prophets like Adam, Noah, Abraham, Moses, Jesus,
