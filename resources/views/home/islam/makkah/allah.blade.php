        <i>
            <h5>Salah (Prayer) :</h5>
        </i> Muslims perform five daily prayers facing the Kaaba in Mecca, reinforcing their
        submission to Allah.

        <i>
            <h5>Dua (Supplication) :</h5>
        </i> Muslims communicate with Allah directly through personal prayers, seeking His help,
        guidance, and forgiveness.

        <i>
            <h5>Fasting (Sawm) during Ramadan :</h5>
        </i> Fasting is a means of attaining spiritual closeness to Allah and
        increasing self-discipline.

        <i>
            <h5>Zakat (Charity) :</h5>
        </i> Muslims give a portion of their wealth to those in need, as a form of gratitude to
